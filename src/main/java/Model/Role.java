package Model;


public class Role {
    private int ID;
    private String name;
    private int type;
    private int status;

    public Role() {
    }

    public Role(int ID, String name, int type, int status) {
        this.ID = ID;
        this.name = name;
        this.type = type;
        this.status = status;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
}
